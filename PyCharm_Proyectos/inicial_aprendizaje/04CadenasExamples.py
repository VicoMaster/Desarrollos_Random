# Ejemplos de Usos en Cadenas Python
print("CARACTERES DE ESCAPE")
frase = "Ella me dijo Hola"
print(frase)
frase = "Ella me dijo,\nHola"
print(frase)
frase = "Ella me dijo \"HOLA!\""
print(frase)
frase = 'Ella me dijo "Hola!"'
print(frase)

# Concatenar
emocion = "Y me emocione"
print(frase+" "+emocion)
# Repetir Cadenas
print((emocion+" ")*3)
